#include <iostream>
using namespace std;

int main() {
    float angka, angka2;
    cout << "masukan angka1: " << endl;
    cin >> angka;
    cout << "masukan angka2: " << endl;
    cin >> angka2;

    cout <<"penjumlahan : " << angka + angka2 << endl;
    cout << "pengurangan : " << angka - angka2 << endl;
    cout << "perkalian : " << angka * angka2 << endl;
    cout << "pembagian : " << angka / angka2 << endl;
    return 0;
}